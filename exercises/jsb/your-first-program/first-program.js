// Task 1
let myNumber = 10;          // number
let myString = "JavaScript"; // string
let myBool = false;         // boolean

console.log(myNumber, typeof myNumber); // 10 "number"
console.log(myString, typeof myString); // "JavaScript" "string"
console.log(myBool, typeof myBool);     // false "boolean"
// Note: lines are always enclosed in single or double quotation marks.

// Task 2
console.log(2 + 3 * 4);     // 14 (first multiplication, then addition)
console.log((2 + 3) * 4);   // 20 (parentheses change the order of execution)
// Parentheses affect the order of operations: without them, multiplication comes first; with them, addition comes first.

